function encodeAnswers(answers) {
  return answers.map(a => a.weight).join('')
}

function decodeAnswers(encoded) {
  if (!encoded || encoded.length !== 20) return null
  const weights = encoded.split('').map(Number)
  if (weights.some(w => w < 1 || w > 5)) return null
  return QUESTIONS.map((q, i) => ({ dim: q.dim, weight: weights[i] }))
}

function calcResults(answers) {
  // ── Raw dim scores: 7 Q × 1–5 weight → range 7–35 per dim ──────────────────
  const dimScores = { 1: 0, 2: 0, 3: 0, 4: 0 }
  answers.forEach(a => { dimScores[a.dim] += a.weight })

  // ── Sort dims by score descending; tie-break by dim id descending (D>C>B>A)
  //    so the same set of scores always produces the same primary/secondary.
  const sorted = [1, 2, 3, 4]
    .map(id => ({ id, score: dimScores[id] }))
    .sort((a, b) => b.score - a.score || b.id - a.id)

  const [rank1, rank2, , rank4] = sorted
  const p  = rank1.id     // primary dim id   (1=A 2=B 3=C 4=D)
  const s  = rank2.id     // secondary dim id
  const ps = rank1.score  // primary score
  const ss = rank2.score  // secondary score

  //  Thresholds  ─────────────────────────────────────────────────────────────
  //  HIGH    ≥ 22  ( 54 % of range 7–35 )  → "高"
  //  EXTREME ≥ 28  ( 75 % of range 7–35 )  → "極高"
  //  SEC_HI  ≥ 20  secondary must reach this to be called "高"
  const HIGH    = 22
  const EXTREME = 28
  const SEC_HI  = 20

  // ── Routing table — matches the 12-archetype specification exactly ──────────
  let profileKey

  //  Guard: all dims calm (primary below HIGH) → Secure
  if (ps < HIGH) {
    profileKey = 'secure'
  }
  // A極高 (≥28) → 微光的殉道者：燃燒自我的融合焦慮
  else if (p === 1 && ps >= EXTREME) {
    profileKey = 'ac_candlelit'
  }
  // C極高 (≥28) → 孤獨的領跑者：必須不斷優秀才能存活
  else if (p === 3 && ps >= EXTREME) {
    profileKey = 'c_lonely'
  }
  // D極高 (≥28) + secondary NOT elevated → 失訊的預言家：心理抽離
  else if (p === 4 && ps >= EXTREME && ss < HIGH) {
    profileKey = 'd_static'
  }
  // A高 + B高 → 曠野的復讀機：渴望與推開的矛盾循環
  else if (p === 1 && s === 2 && ss >= SEC_HI) {
    profileKey = 'ab_wilderness'
  }
  // A高 + D高 → 暫停的焦慮家：高度警覺導致的情緒過載
  else if (p === 1 && s === 4 && ss >= SEC_HI) {
    profileKey = 'a_timeless'
  }
  // A高 + D(最低) → 斷線的呼喚者：飢渴的單向溝通與回應成癮
  else if (p === 1 && rank4.id === 4) {
    profileKey = 'ad_severed'
  }
  // A高 (remaining) → 暫停的焦慮家 (fallback)
  else if (p === 1) {
    profileKey = 'a_timeless'
  }
  // C高 + A高 → 華麗的受難者：透過完美表演索求安全感
  else if (p === 3 && s === 1) {
    profileKey = 'ca_masked'
  }
  // C高 + D高 → 永存的瞬間：溺於舊傷中的情感反芻
  else if (p === 3 && s === 4 && ss >= SEC_HI) {
    profileKey = 'dc_snapshot'
  }
  // C高 (remaining) → 孤獨的領跑者 (fallback)
  else if (p === 3) {
    profileKey = 'c_lonely'
  }
  // B高 + C高 → 永恆的觀測者：渴望被看見卻恐懼被入侵
  else if (p === 2 && s === 3 && ss >= SEC_HI) {
    profileKey = 'b_eternal'
  }
  // B高 (remaining) → 永恆的觀測者 (fallback)
  else if (p === 2) {
    profileKey = 'b_eternal'
  }
  // D高 + B高 → 溺水的迴聲：壓力下的自我封閉（凍結）
  else if (p === 4 && s === 2) {
    profileKey = 'da_drowning'
  }
  // D高 + C高 → 規訓的流放者：用理智隔絕早年的情感痛楚
  else if (p === 4 && s === 3) {
    profileKey = 'db_scholar'
  }
  // D高 (remaining) → 失訊的預言家 (fallback)
  else {
    profileKey = 'd_static'
  }

  const profile = PROFILES[profileKey] || PROFILES['secure']

  // ── Per-dim health: 100 = most regulated, 0 = most activated ───────────────
  const dimData = DIMENSIONS.map(dim => {
    const score  = dimScores[dim.id]
    const health = Math.round(100 - ((score - 7) / 28) * 100)
    const pct    = 100 - health
    return { ...dim, score, health, pct }
  })

  // ── 診斷代碼 ────────────────────────────────────────────────────────────────
  //  Format: KM-XX-A#B#C#D#
  //  Score buckets per dim:  1 (≤11) 2 (12–16) 3 (17–21) 4 (22–27) 5 (28–35)
  const buckets = [1, 2, 3, 4].map(id => {
    const sc  = dimScores[id]
    const bkt = sc >= 28 ? 5 : sc >= 22 ? 4 : sc >= 17 ? 3 : sc >= 12 ? 2 : 1
    return ['A','B','C','D'][id - 1] + bkt
  }).join('')
  const diagCode = `${DIAG_CODE_MAP[profileKey] ?? 'KM-00'}-${buckets}`

  // ── 雷達圖數據 (0=min activation, 100=max activation) ──────────────────────
  const radarData = DIMENSIONS.map(dim => ({
    ...dim,
    score: dimScores[dim.id],
    pct:   Math.round(((dimScores[dim.id] - 7) / 28) * 100),
  }))

  return { profile, dimData, primaryDim: p, profileKey, dimScores, diagCode, radarData }
}

// Reverse-lookup map: 'KM-01' → 'secure'
const CODE_TO_PROFILE = Object.fromEntries(
  Object.entries(DIAG_CODE_MAP).map(([k, v]) => [v, k])
)
// Bucket midpoints (score range 7–35, bucket 1–5)
const BUCKET_MID = { 1: 9, 2: 14, 3: 19, 4: 25, 5: 31 }

function parseCode(raw) {
  const code = raw.trim().toUpperCase()
  // Format: KM-XX-A{1-5}B{1-5}C{1-5}D{1-5}
  const m = code.match(/^(KM-\d{2})-A([1-5])B([1-5])C([1-5])D([1-5])$/)
  if (!m) return null
  const profileKey = CODE_TO_PROFILE[m[1]]
  const profile = profileKey ? PROFILES[profileKey] : null
  if (!profile) return null
  const dimScores = { 1: BUCKET_MID[+m[2]], 2: BUCKET_MID[+m[3]], 3: BUCKET_MID[+m[4]], 4: BUCKET_MID[+m[5]] }
  const dimData = DIMENSIONS.map(dim => {
    const score = dimScores[dim.id]
    const health = Math.round(100 - ((score - 7) / 28) * 100)
    const pct    = 100 - health
    return { ...dim, score, health, pct }
  })
  const radarData = DIMENSIONS.map(dim => ({
    ...dim, score: dimScores[dim.id],
    pct: Math.round(((dimScores[dim.id] - 7) / 28) * 100),
  }))
  return { profile, dimData, primaryDim: DIMENSIONS[0], profileKey, dimScores, diagCode: code, radarData }
}

